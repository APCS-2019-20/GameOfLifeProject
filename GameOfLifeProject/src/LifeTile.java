
public class LifeTile
 	{
		String phrase; 
		int amount;
		
		public LifeTile(String p, int a)
		{ 
			phrase = p; 
			amount = a; 
		}

		public String getPhrase() {
			return phrase;
		}

		public void setPhrase(String phrase) {
			this.phrase = phrase;
		}

		public int getAmount() {
			return amount;
		}

		public void setAmount(int amount) {
			this.amount = amount;
		}

	}
